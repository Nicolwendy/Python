 #list data structures
fruits=["mangoes","oranges","watermelon","pineapple"]
fruits[2]="apples"
print(f"I love eating {fruits}")
print(f"I love eating {fruits[2]}.")

#list is ordered
#LIST IS CHANGEABLE
#List has indexes

#tuple data structures
cars=("Chevrolet","nissan","Mercedes","audi",15,True)
print(f"{cars}")
print(f"My dream car is {cars[2]}")

#Tuples are ordered.
#Tuples have indexes.
#Tuples are unchangeable.

#SET DATA STRUCTURES
computers={"hp",'lenovo',"toshiba","dell"}
print(f"{computers}")

#Set is unordered.
#Set doesn't have indexes.

#dictionary data structures
staff={"Name":"Wendy","Age":"20","Gender":"Female"}
print(f"{staff}")

print(f"Staff name:{staff["Name"]}")
print(f"Staff age:{staff["Age"]}")
print(f"Staff gender:{staff["Gender"]}")



